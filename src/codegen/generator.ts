function validationSQL(plan: TablePlan, allMetrics: MetricPlan[], sm: SourceMapping): string {
  const checks: string[] = [];
  checks.push(`${"-".repeat(71)}`);
  checks.push(`-- Validation`);
  checks.push(`${"-".repeat(71)}`);

  const hasSummarization = plan.grainGroups.some((g) => g.needsSummarization);
  let n = 1;

  // Determine if time is being summarized out of this table
  const bftGrainSet = new Set(plan.bftGrain);
  const summarizesTime = plan.timePlan != null && hasSummarization &&
    plan.grainGroups.some((g) => g.needsSummarization &&
      g.grain.some((e) => plan.timePlan!.timeDerivedEntities.has(e) && !bftGrainSet.has(e)));

  // V: SUM check for each additive metric
  for (const metric of allMetrics) {
    if (metric.nature === "non-additive") continue;
    // Stock metrics with time summarized out won't match source SUM (intentionally)
    if (metric.stock && summarizesTime) continue;

    const home = homeEntity(metric);
    // For relationship metrics, the source table is the junction table
    const sourceTable = metric.home.kind === "relationship"
      ? sm.relationships[metric.home.name].table
      : sm.entities[home].table;

    checks.push("");
    checks.push(`-- V${n}: SUM(${metric.name}) matches source`);
    checks.push(
      `SELECT 'V${n} ${metric.name}_sum' AS test,`,
      `       CASE WHEN ABS(SUM(${q(metric.name)}) - (SELECT SUM(${q(metric.sourceColumn)}) FROM ${q(sourceTable)})) < 0.01`,
      `            THEN 'PASS' ELSE 'FAIL: ' || SUM(${q(metric.name)}) END AS result`,
      `FROM ${q(plan.tableName)};`
    );
    n++;
  }

  // V: Per-entity SUM check for allocated metrics (only when no summarization)
  if (!hasSummarization) {
    for (const metric of allMetrics) {
      const hasAllocDims = metric.propagatedDimensions.some((d) => d.strategy === "allocation");
      if (!hasAllocDims || metric.reserveDimensions.length > 0) continue;

      const home = homeEntity(metric);
      const homeEs = sm.entities[home];
      const sourceTable = metric.home.kind === "relationship"
        ? sm.relationships[metric.home.name].table
        : homeEs.table;

      if (metric.home.kind === "relationship") {
        // For relationship metrics, per-home-entity check doesn't apply the same way
        // (the source is junction rows, not entity rows). Skip for now.
        continue;
      }

      checks.push("");
      checks.push(`-- V${n}: ${metric.name} per ${home} sums to original`);
      checks.push(
        `SELECT 'V${n} ${metric.name}_per_${home.toLowerCase()}' AS test,`,
        `       CASE WHEN COUNT(*) = 0 THEN 'PASS'`,
        `            ELSE 'FAIL: ' || COUNT(*) || ' with wrong sum' END AS result`,
        `FROM (`,
        `    SELECT t.${q(homeEs.idColumn)}, ABS(SUM(t.${q(metric.name)}) - s.${q(metric.sourceColumn)}) AS err`,
        `    FROM ${q(plan.tableName)} t`,
        `    JOIN ${q(homeEs.table)} s ON t.${q(homeEs.idColumn)} = s.${q(homeEs.idColumn)}`,
        `    GROUP BY t.${q(homeEs.idColumn)}, s.${q(metric.sourceColumn)}`,
        `    HAVING ABS(SUM(t.${q(metric.name)}) - s.${q(metric.sourceColumn)}) > 0.01`,
        `);`
      );
      n++;
    }
  }

  // V: Sum/Sum weight check (only for metrics at their natural grain —
  // propagated non-additive metrics have weights partitioned differently)
  for (const metric of allMetrics) {
    if (metric.nature !== "non-additive") continue;
    const hasPropagation = metric.propagatedDimensions.some((d) => d.strategy !== "reserve");
    if (hasPropagation) continue;

    const home = homeEntity(metric);
    const homeEs = sm.entities[home];
    checks.push("");
    checks.push(`-- V${n}: ${metric.name}_weight sums to 1.0 per ${home}`);
    checks.push(
      `SELECT 'V${n} ${metric.name}_weights' AS test,`,
      `       CASE WHEN COUNT(*) = 0 THEN 'PASS'`,
      `            ELSE 'FAIL: ' || COUNT(*) || ' with bad weights' END AS result`,
      `FROM (`,
      `    SELECT ${q(homeEs.idColumn)}, ABS(SUM(${q(metric.name + "_weight")}) - 1.0) AS err`,
      `    FROM ${q(plan.tableName)}`,
      `    WHERE ${q(homeEs.idColumn)} IS NOT NULL`,
      `    GROUP BY ${q(homeEs.idColumn)}`,
      `    HAVING ABS(SUM(${q(metric.name + "_weight")}) - 1.0) > 0.001`,
      `);`
    );
    n++;
  }

  return checks.join("\n");
}

// ---------------------------------------------------------------------------
